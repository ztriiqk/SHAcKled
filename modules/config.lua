function TableToString(tbl, indent)
    indent = indent or 0
    local str = ""
    local keys = {}
    for key in pairs(tbl) do
    table.insert(keys, key)
    end
    table.sort(keys, function(a, b) return tostring(a) < tostring(b) end)
    for _, key in ipairs(keys) do
    local value = tbl[key]
    local indentation = string.rep("  ", indent)
        if type(value) == "table" then
            if type(key) == "number" or type(key) == "boolean" then
                str = str .. indentation .. "[" .. tostring(key) .. "] = {\n" .. TableToString(value, indent + 1) .. indentation .. "},\n"
            else
                str = str .. indentation .. key .. " = {\n" .. TableToString(value, indent + 1) .. indentation .. "},\n"
            end
        elseif type(value) == "boolean" then
            if type(key) ~= "string" and type(key) ~= "boolean" then
                str = str .. indentation .. "[" .. tostring(key) .. "] = ImBool(" .. tostring(value) .. "),\n"
            else
                str = str .. indentation .. key .. " = ImBool(" .. tostring(value) .. "),\n"
            end
        elseif type(value) == "number" then
            local valueString
            if tostring(value):find("%.") ~= nil or tostring(value):find("e") ~= nil then
                valueString = "ImFloat(" .. string.format("%.4f", value) .. ")"
            else
                valueString = "ImInt(" .. tostring(value) .. ")"
            end
            if type(key) == "number" then
                str = str .. indentation .. "[" .. tostring(key) .. "] = " .. valueString .. ",\n"
            else
                str = str .. indentation .. key .. " = " .. valueString .. ",\n"
            end
        elseif type(value) == "string" then
            if type(key) == "number" then
                str = str .. indentation .. "[" .. tostring(key) .. "] = ImBuffer(\"" .. value .. "\",200),\n"
            else
                str = str .. indentation .. key .. " = ImBuffer(\"" .. value .. "\",200),\n"
            end
        else
            str = str .. indentation .. key .. " = " .. string.format("%q", tostring(value)) .. ",\n"
        end
    end
    return str
end
